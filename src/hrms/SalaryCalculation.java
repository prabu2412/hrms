package hrms;

public class SalaryCalculation {

	private int salaryCalculationId;
	
	private String EmployeeNumber;
	
	private int permissionManagementId;
	
	private String salaryMonth;
	
	private String salaryAmount;
	
	private String bonous;
	
	private String totalSalary;
	
	private String year;

	public int getSalaryCalculationId() {
		return salaryCalculationId;
	}

	public void setSalaryCalculationId(int salaryCalculationId) {
		this.salaryCalculationId = salaryCalculationId;
	}


	public String getEmployeeNumber() {
		return EmployeeNumber;
	}

	public void setEmployeeNumber(String employeeNumber) {
		EmployeeNumber = employeeNumber;
	}

	public int getPermissionManagementId() {
		return permissionManagementId;
	}

	public void setPermissionManagementId(int permissionManagementId) {
		this.permissionManagementId = permissionManagementId;
	}

	public String getSalaryMonth() {
		return salaryMonth;
	}

	public void setSalaryMonth(String salaryMonth) {
		this.salaryMonth = salaryMonth;
	}

	public String getSalaryAmount() {
		return salaryAmount;
	}

	public void setSalaryAmount(String salaryAmount) {
		this.salaryAmount = salaryAmount;
	}

	public String getBonous() {
		return bonous;
	}

	public void setBonous(String bonous) {
		this.bonous = bonous;
	}

	public String getTotalSalary() {
		return totalSalary;
	}

	public void setTotalSalary(String totalSalary) {
		this.totalSalary = totalSalary;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}
	
	
}
