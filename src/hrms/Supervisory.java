package hrms;

public class Supervisory {


	int supervisoryId;
	
	String supervisoryName;
	
	String departmentHead;
	
	String supervisoryDateOfJoining;
	
	String supervisoryNumber;
	
	String supervisoryMobile;
	
	String supervisoryMail;

	public int getSupervisoryId() {
		return supervisoryId;
	}

	public void setSupervisoryId(int supervisoryId) {
		this.supervisoryId = supervisoryId;
	}

	public String getSupervisoryName() {
		return supervisoryName;
	}

	public void setSupervisoryName(String supervisoryName) {
		this.supervisoryName = supervisoryName;
	}

	public String getDepartmentHead() {
		return departmentHead;
	}

	public void setDepartmentHead(String departmentHead) {
		this.departmentHead = departmentHead;
	}

	public String getSupervisoryDateOfJoining() {
		return supervisoryDateOfJoining;
	}

	public void setSupervisoryDateOfJoining(String supervisoryDateOfJoining) {
		this.supervisoryDateOfJoining = supervisoryDateOfJoining;
	}

	public String getSupervisoryNumber() {
		return supervisoryNumber;
	}

	public void setSupervisoryNumber(String supervisoryNumber) {
		this.supervisoryNumber = supervisoryNumber;
	}

	public String getSupervisoryMobile() {
		return supervisoryMobile;
	}

	public void setSupervisoryMobile(String supervisoryMobile) {
		this.supervisoryMobile = supervisoryMobile;
	}

	public String getSupervisoryMail() {
		return supervisoryMail;
	}

	public void setSupervisoryMail(String supervisoryMail) {
		this.supervisoryMail = supervisoryMail;
	}


	
}
